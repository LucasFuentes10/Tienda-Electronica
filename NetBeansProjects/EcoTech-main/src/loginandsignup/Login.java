    private boolean validarCredenciales(String usuario, String contraseña) {
        try {
            File archivo = new File("Usuarios.ini");
            if (!archivo.exists()) {
                JOptionPane.showMessageDialog(this, "No existe el archivo de usuarios.");
                return false;
            }

            Wini ini = new Wini(archivo);
            for (int i = 0; ; i++) {
                String seccion = "usuario" + i;
                if (!ini.containsKey(seccion)) break;

                String nombre = ini.get(seccion, "nombre");
                String pass = ini.get(seccion, "contraseña");
                String categoria = ini.get(seccion, "categoria");

                if (nombre.equals(usuario) && pass.equals(contraseña)) {
                    abrirInterfazPorCategoria(categoria);
                    return true;
                }
            }
        } catch (IOException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al leer el archivo de usuarios.");
        }
        return false;
    }
    
    private void abrirInterfazPorCategoria(String categoria) {
        JOptionPane.showMessageDialog(this, "Bienvenido, que tenga un excelente día");

        if (categoria.equalsIgnoreCase("Admin")) {
            Tienda_electronica_Interfaz adminFrame = new Tienda_electronica_Interfaz();
            adminFrame.setVisible(true);
            adminFrame.setLocationRelativeTo(null);
        } else if (categoria.equalsIgnoreCase("Empleado")) {
            InterfazProducto empleadoFrame = new InterfazProducto();
            empleadoFrame.setVisible(true);
            empleadoFrame.setLocationRelativeTo(null);
        } else if (categoria.equalsIgnoreCase("Usuario")) {
            ProductoUsuario2 usuarioFrame = new ProductoUsuario2();
            usuarioFrame.setVisible(true);
            usuarioFrame.setLocationRelativeTo(null);
        }

        this.dispose();
    }
    
